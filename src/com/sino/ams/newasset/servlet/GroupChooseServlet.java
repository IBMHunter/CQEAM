public class GroupChooseServlet extends BaseServlet {

    public void performTask(HttpServletRequest req, HttpServletResponse res) throws
            ServletException, IOException {
        String forwardURL = "";
        Message message = SessionUtil.getMessage(req);
        Connection conn = null;
        try {
            SfUserDTO user = (SfUserDTO) getUserAccount(req);
            Request2DTO req2DTO = new Request2DTO();
            req2DTO.setDTOClassName(AmsAssetsTransHeaderDTO.class.getName());
            conn = getDBConnection(req);
            AmsAssetsTransHeaderDTO dto = (AmsAssetsTransHeaderDTO) req2DTO.getDTO(req);
            dto.setServletConfig(getServletConfig(req));
            String sfProject = StrUtil.nullToString(req.getParameter("sf_project"));
            String sfGroup = StrUtil.nullToString(req.getParameter("sf_group"));
            String sfRole = StrUtil.nullToString(req.getParameter("sf_role"));
            AssetsOptProducer optProducer = new AssetsOptProducer(user, conn);
//            String groupOptions = optProducer.getUserGroupOption(dto);
            String groupOptions = optProducer.getUserGroupOption2(sfProject, sfGroup, sfRole);
            req.setAttribute(AssetsWebAttributes.GROUP_OPTIONS, groupOptions);
            forwardURL = AssetsURLList.ASSETS_GROUP_SELECT;
        } catch (DTOException ex) {
            ex.printLog();
            message = getMessage(MsgKeyConstant.DTO_ERROR);
            message.setIsError(true);
            forwardURL = MessageConstant.MSG_PRC_SERVLET;
        } catch (QueryException ex) {
            ex.printLog();
            message = getMessage(MsgKeyConstant.DTO_ERROR);
            message.setIsError(true);
            forwardURL = MessageConstant.MSG_PRC_SERVLET;
        } catch (PoolPassivateException ex) {
			ex.printLog();
			message = getMessage(AssetsMessageKeys.POOL_PASSIVATE_ERROR);
			message.setIsError(true);
			forwardURL = MessageConstant.MSG_PRC_SERVLET;
		} finally {
            closeDBConnection(conn);
            setHandleMessage(req, message);
            ServletForwarder forwarder = new ServletForwarder(req, res);
            forwarder.forwardView(forwardURL);
            //根据实际情况修改页面跳转代码。
        }
    }
}
