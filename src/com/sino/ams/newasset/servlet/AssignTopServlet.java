public class AssignTopServlet extends BaseServlet {

    /**
     * 所有的Servlet都必须实现的方法。
     * @param req HttpServletRequest
     * @param res HttpServletResponse
     * @throws ServletException
     * @throws IOException
     */
    public void performTask(HttpServletRequest req, HttpServletResponse res) throws
            ServletException, IOException {
        String forwardURL = "";
        Message message = SessionUtil.getMessage(req);
        try {
            SfUserDTO user = (SfUserDTO) getUserAccount(req);
            AssetsRadioProducer radioProducer = new AssetsRadioProducer(user);
            String assignProp = AssetsWebAttributes.ASSIGN_MAINTAIN_USER;
            req.setAttribute(AssetsWebAttributes.ASSIGN_RADIO,
                             radioProducer.getAssignRadio(assignProp));
            forwardURL = AssetsURLList.ASSETS_ASSIGN_TOP;
        } catch (HandlerException ex) {
            ex.printLog();
            forwardURL = MessageConstant.MSG_PRC_SERVLET;
            message = getMessage(AssetsMessageKeys.COMMON_ERROR);
        } finally {
            setHandleMessage(req, message);
            ServletForwarder forwarder = new ServletForwarder(req, res);
            forwarder.forwardView(forwardURL);
        }
    }
}
